num1 = int(input("enter a number"))
num2 = int(input("enter a number"))
Oper = input("choose an operator(+,-,*,/)")
if Oper == "+":
    print("num1+num2")
elif Oper == "-":
    print("num1-num2")
elif Oper == "*":
    print("num1*num2")
elif Oper == "/":
    print("num1/num2")
else:
    print("complete")
